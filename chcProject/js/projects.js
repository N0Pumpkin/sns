// Рендерим карточки
const projectsList = document.getElementById('projectsGrid');
projects.forEach((p, idx) => {
    const card = document.createElement('div');
    card.className = "project-card";
    card.innerHTML = `
        <div class="project-title">${p.name}</div>
        <div class="project-meta">
            <span class="project-client">${p.client}</span>
            <span class="project-period">${p.period}</span>
        </div>
    `;
    card.onclick = () => showOverlay(idx);
    projectsList.appendChild(card);
});

// Overlay логика
const overlay = document.getElementById('projectOverlay');
const closeOverlayBtn = document.getElementById('closeOverlay');

function showOverlay(idx) {
    const p = projects[idx];
    document.getElementById('overlayTitle').textContent = p.name;
    document.getElementById('overlayClient').textContent = p.client;
    document.getElementById('overlayPeriod').textContent = p.period;
    document.getElementById('overlayDesc').textContent = p.desc;
    overlay.classList.add('open');
}

closeOverlayBtn.onclick = () => overlay.classList.remove('open');
overlay.onclick = (e) => { if (e.target === overlay) overlay.classList.remove('open'); }
document.addEventListener('keydown', e => {
    if (overlay.classList.contains('open') && e.key === "Escape") overlay.classList.remove('open');
});
